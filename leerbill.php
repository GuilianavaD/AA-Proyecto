    Bill alcanzó la sobriedad el 11 de diciembre de 1934 y la mantuvo durante más de 36 años. Sin embargo este
     éxito no fue ajeno a dificultades o episodios de conducta compulsiva o depresión. <p>
     Bill W., cofundador de Alcohólicos Anónimos, escribió más de 150 artículos entre los años 1944 y 1970. Este libro contiene 
   todos esos artículos en un volumen, incluidos sus primeros pensamientos sobre las Tradiciones, sus batallas contra
    la depresión crónica y el orgullo espiritual, los recuerdos de una juerga durante toda la noche con Ebby y una vívida
     descripción de cómo llegó a organizar los Pasos. (Habían solo seis en el primer borrador).<p>
     En sus últimos años experimentó con otras posibles curas para el alcoholismo: el LSD, la niacina o vitamina B3 y la parapsicología para inducir 
      un cambio espiritual.Bill murió de enfisema complicado por neumonía en 1971. <p>Su mujer, Lois Wilson (Lois W.),
       fue la fundadora de Al-Anon, un grupo dedicado al auxilio de los amigos y familiares de alcohólicos. En 1999 Time
        Magazine incluyó a Bill entre los veinte primeros del Time 100: Héroes e iconos que ejemplifican "coraje, autodominio,
        exuberancia, habilidad sobrehumana y gracia maravillosa" en el siglo XX.<p>
