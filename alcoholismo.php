        <h2 style="color:red";>
        ¿Qué es el alcoholismo?</h2></br>
        El alcoholismo es una enfermedad que consiste
        en padecer una fuerte necesidad de ingerir
        sustancias que contenen alcohol, de forma que
        existe una dependencia física del mismo,
        manifestada a través de determinados síntomas
        de abstinencia cuando se suspende su consumo.</p><br>
        El alcohólico no tiene control sobre los límites de
        su consumo, y suele ir elevando a lo largo del
        tiempo su grado de tolerancia al alcohol.<p><br></br>
