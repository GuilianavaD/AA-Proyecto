<h2> 1. Trastorno por adicción al alcohol o alcoholismo</h2><p><br>
Una de las enfermedades enfermedades psiquiátricas más graves que produce su consumo es el propio alcoholismo.
 Es un trastorno que convierte a la persona en un esclavo de esta sustancia mientras además, destruye su organismo. 
 <br> Se caracteriza por:<p>
 <br>
*Evidencia subjetiva de compulsión para la utilización del alcohol durante los intentos de abandono del consumo. 
Su vida queda centrada en el deseo compulsivo de la obtención y consumo de alcohol. <p><br>
*El deseo de abandonarlo.<p><br>
*Tomar es un hábito estereotipado, un patrón de consumo y un estilo de vida.<p><br>
*Evidencia de neuroadaptación, con fenómenos de tolerancia y dependencia.<p><br>
*Se consume para aliviar o evitar los síntomas del síndrome de abstinencia alcohólica.<p><br>
*Predominio de la conducta de búsqueda y obtención del alcohol por encima de otras prioridades importantes.<p><br>
*Una rápida instauración de la dependencia si después de un periodo de abstinencia el paciente vuelve a consumir. <p><br>
 
<br>
<h2>2. Enfermedades del sistema nervioso</h2><p><br>
Es un depresor del sistema nervioso central. Por tanto puede provocar, entre otras, enfermedades graves como:
<p><br>
*Depresión<p><br>
*Insomnio<p><br>
*Ataques de pánico<p><br>
*Celotipia alcohólica (es común desarrollar unos celos enfermizos con el consumo de alcohol)<p><br>
*Ansiedad<p><br>
*Demencias neurodegenerativas como la enfermedad de Alzheimer o Parkinson. <p><br>
*Deterioro cognitivo, pérdida de funciones cerebrales claves como la atención, la memoria, la dedicación, 
la motivación, la toma de decisiones, etc.<p><br>
*La sobredosis de alcohol puede producir daños cerebrales: <p><br>
&nbsp;&nbsp;&nbsp;&nbsp;*Neuropatía alcohólica<p><br>
&nbsp;&nbsp;&nbsp;&nbsp;*Demencia alcohólica. Síndrome de Korsakoff y Wernicke. <p><br>
<h2>3. Enfermedades cardiovasculares</h2><p><br>
Las personas que abusan del etanol tienen un mayor riesgo de, entre otros:<br>
*Hipertensión arterial<br>
*Accidentes cerebrovasculares <p><br>
*Insuficiencia cardíaca, arritmia<p><br>
*Miopatía alcohólica aguda y crónica <p><br>
*Miocardiopatía alcohólica. El infarto. <br>
<h2>4. Cáncer</h2><p><br>
Entre otros, provoca:<p><br>

*Cáncer de esófago<p><br>
*Cáncer de estómago<p><br>
*De colon y  recto<p><br>
*Cáncer de hígado (debido a su implicación en la metabolización del alcohol, es el órgano más directamente 
dañado y relacionado con un mayor número de muertes).<p><br>
*De la cavidad oral <p><br>
*Cáncer de pulmón  <p><br>
*Cáncer de mama<p><br>
 <br>

<h2>5. Enfermedades del sistema digestivo</h2><p><br>
Está directamente relacionado con el cáncer de todo el aparato digestivo. 

Afecta a todo el aparato digestivo produciendo desde inflamaciones hasta hepatitis y pancreatitis. Puede desarrollar:
&nbsp;&nbsp;&nbsp;&nbsp;<img style="border-radius: 20px;" src="imagen/unnamed (1).jpg" align="right" width="400px" height="200" alt="" ><br>
*Pancreatitis crónica<p><br>
*Hígado graso<p><br>
*Hepatitis alcohólica<p><br>
 
<br>
<h2>6. Intoxicación alcohólica aguda</h2><p><br>
Necesita atención médica inmediata. Afecta el sistema nervioso. Es una pérdida de control temporal tanto físico como mental
 que puede llegar al coma etílico, con riesgo de muerte. <br>

Los síntomas serían: inconsciencia, ausencia de reflejos, hipotensión, presión arterial baja e hipotermia. Se puede complicar
 por posible traumatismo cráneo-encefálico a consecuencia de una caída, intoxicaciones mixtas, neumonía etc. Puede producir parálisis. <br>
<br>
