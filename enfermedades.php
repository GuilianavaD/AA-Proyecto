      Enfermedad crónica caracterizada por la ingesta descontrolada de alcohol y preocupación por el consumo.
El alcoholismo es la incapacidad de controlar el consumo de alcohol debido a una dependencia física y emocional.
Los síntomas incluyen consumo recurrente de alcohol a pesar de los problemas legales y de salud relacionados.
 Las personas alcohólicas pueden beber al comienzo del día, sentirse culpables por el consumo y tener el deseo
  de reducir la cantidad de alcohol que ingieren.
El tratamiento incluye el asesoramiento por parte de un profesional del cuidado de la salud. Una opción para
 aquellos que necesitan ayuda adicional son los programas de desintoxicación en hospitales o instalaciones médicas.
  Existen medicamentos que reducen el deseo de consumir alcohol.<br>
