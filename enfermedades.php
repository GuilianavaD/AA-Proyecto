<p><br>
1.-Traumatismos craneoencefálicos<p><br>
2.-Disfunciones sexuales <p><br>
3.-Desnutrición y anemia<p><br>
4.-Accidentes de circulación y muerte de terceros <p><br>
5.-Síndrome alcohólico fetal o SAF (consumir alcohol en el embarazo puede causar un conjunto de anormalidades en el feto)<p><br>
 

<h2>8. Enfermedad social</h2><p><br>
Podríamos considerar el abuso de alcohol como una enfermedad social, debido al grave impacto que causa en el entorno del paciente.
 Está detrás de numerosas agresiones, violencia, peleas, destrozos materiales, absentismo laboral, accidentes domésticos, accidentes 
 laborales, incendios, ahogos y suicidios.  <br>

 <br>

<h2>9. Enfermedad familiar</h2><br><p>
Si la sociedad se ve afectada por el consumo de alcohol, este también tiene graves consecuencias en la familia del adicto.
<br>
<h2>1. La codependencia</h2><br>
El alcoholismo provoca también una enfermedad familiar llamada codependencia. Se da en algunas de las personas cercanas al alcohólico. 
El alcohol hace enfermar a toda una familia. En algún miembro de esta familia puede llegar a ser una verdadera patología. <br>
Entre sus síntomas encontramos:<p><br>

*Depresión<p><br>
*Nerviosismo<p><br>
*Ansiedad<p><br>
*Automedicación<p><br>
*Baja autoestima<p><br>
*Excesiva dependencia<p><br>
*Imposibilidad de poner límites<p><br>
*Compulsividad<p><br>
*Inestabilidad emocional<p><br>
Otros<p><br>
Esta enfermedad requiere de un tratamiento y de ayuda psicológica.<p><br>

<h2>2. Rupturas de pareja</h2><p><br>
La inestabilidad emocional sufrida por el alcohólico, los problemas de comunicación y el agotamiento de los recursos afectivos
 destruyen familias. <p><br>

<h2>3. Trastornos en los hijos</h2><p><br>
 
<br>
Si bebes habitualmente o bebes mucho y no sabes si has desarrollado el alcoholismo, acude a tu médico. En cualquier caso,
 si no lo has desarrollado, lo más sano sería reducir el consumo para evitar la larga lista de consecuencias que acabamos de presentarte.
